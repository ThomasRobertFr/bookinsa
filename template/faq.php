<h2 id="q1">Comment se passe un prêt ?</h2>

<ul class="faq">
	<li class="faqfirst">
		<div class="num">1</div>
		<p>Sur la page d'un livre, un utilisateur clique sur « demander le prêt »</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-1.png" title="1. Sur la page d'un livre, un utilisateur clique sur « demander le prêt »"><img src="images/res/pics/faq1-1.png" /></a></p>
	</li>
	<li>
		<div class="num">2</div>
		<p>Il crée alors une nouvelle demande, c'est à dire une conversation entre lui et le possesseur du livre. Pour se faire, il commence par écrire un premier message au possesseur du livre.</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-2.png" title="2. Il crée alors une nouvelle demande, c'est à dire une conversation entre lui et le possesseur du livre. Pour se faire, il commence par écrire un premier message au possesseur du livre."><img src="images/res/pics/faq1-2.png" /></a></p>
	</li>
	<li class="faqfirst">
		<div class="num">3</div>
		<p>Le possesseur du livre reçoit alors la demande dans ses partages.</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-3.png" title="3. Le possesseur du livre reçoit alors la demande dans ses partages."><img src="images/res/pics/faq1-3.png" /></a></p>
	</li>
	<li>
		<div class="num">4</div>
		<p>Il peut alors répondre à la demande en l'acceptant ou en la refusant.</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-5.png" title="4. Il peut alors répondre à la demande en l'acceptant ou en la refusant."><img src="images/res/pics/faq1-5.png" /></a></p>
	</li>
	<li class="faqfirst">
		<div class="num">5</div>
		<p>Par ailleurs, le système de messagerie permet de se retrouver pour se passer le livre.</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-6.png" title="5. Par ailleurs, le système de messagerie permet de se retrouver pour se passer le livre."><img src="images/res/pics/faq1-6.png" /></a></p>
	</li>
	<li>
		<div class="num">6</div>
		<p>Durant toute la durée du prêt, le système de messagerie permet de se tenir informé de la lecture et se retrouver pour rendre le livre. Une fois le livre rendu, le possesseur le signale dans la conversation afin de rendre le livre a nouveau disponible sur le site.</p>
		<p class="img"><a class="zoombox zgallery1" href="images/res/faq1-7.png" title="6. Durant toute la durée du prêt, le système de messagerie permet de se tenir informé de la lecture et se retrouver pour rendre le livre. Une fois le livre rendu, le possesseur le signale dans la conversation afin de rendre le livre a nouveau disponible sur le site."><img src="images/res/pics/faq1-7.png" /></a></p>
	</li>
	<div class="clear"></div>
</ul>


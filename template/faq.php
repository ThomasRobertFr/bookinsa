<h2 id="q3">Comment ajouter un livre ?</h2>

<ul class="faq">
	<li class="faqfirst">
		<div class="num">1</div>
		<p>Dans la page « Mes livres », cliquez sur le bouton « Ajouter un livre »</p>
		<p class="img"><a class="zoombox zgallery2" href="images/res/faq2-1.png" title="1. Dans la page « Mes livres », cliquez sur le bouton « Ajouter un livre »"><img src="images/res/pics/faq2-1.png" /></a></p>
	</li>
	<li>
		<div class="num">2</div>
		<p>Entrez l'ISBN et le genre du livre.</p>
		<p class="img"><a class="zoombox zgallery2" href="images/res/faq2-2.png" title="2. Entrez l'ISBN et le genre du livre."><img src="images/res/pics/faq2-2.png" /></a></p>
	</li>
	<li class="faqfirst">
		<div class="num">3</div>
		<p>Si le livre n'a pas été trouvé automatiquement, entrez les informations sur le livre manuellement. S'il a été trouvé, les champs sont remplis automatiquement.</p>
		<p class="img"><a class="zoombox zgallery2" href="images/res/faq2-3.png" title="3. Si le livre n'a pas été trouvé automatiquement, entrez les informations sur le livre manuellement. S'il a été trouvé, les champs sont remplis automatiquement."><img src="images/res/pics/faq2-3.png" /></a></p>
	</li>
	<li>
		<div class="num">4</div>
		<p>Choisissez la miniature du livre : soit une des miniatures trouvées automatiquement, soit en entrant un lien vers une image. Puis cliquez sur ajouter pour valider l'ajout du livre.</p>
		<p class="img"><a class="zoombox zgallery2" href="images/res/faq2-4.png" title="4. Choisissez la miniature du livre : soit une des miniatures trouvées automatiquement, soit en entrant un lien vers une image."><img src="images/res/pics/faq2-4.png" /></a></p>
	</li>
	<li>
		<div class="num">5</div>
		<p>Votre livre est ajouté a la liste.</p>
		<p class="img"><a class="zoombox zgallery2" href="images/res/faq2-5.png" title="5. Votre livre est ajouté a la liste."><img src="images/res/pics/faq2-5.png" /></a></p>
	</li>
	<div class="clear"></div>
</ul>





